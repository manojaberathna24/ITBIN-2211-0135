    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
      
        int selectedRow = jTable3.getSelectedRow();
    
    // If no row is selected, display a message and return
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to update.", "No Row Selected", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Get the updated values from the input fields
    String lectureID = lecid.getText();
    String lectureName = lecname.getText();
    String telephone = tele.getText();
    String address = add.getText();
    String selectedCourse = course.getSelectedItem().toString();
    String selectedBranch = branch.getSelectedItem().toString();
    
    // Update the selected row in the table
    DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
    model.setValueAt(lectureID, selectedRow, 0);
    model.setValueAt(lectureName, selectedRow, 1);
    model.setValueAt(telephone, selectedRow, 2);
    model.setValueAt(address, selectedRow, 3);
    model.setValueAt(selectedCourse, selectedRow, 4);
    model.setValueAt(selectedBranch, selectedRow, 5);
    
    // Update the corresponding record in the database
    try {
        // Assuming you have a method to update a lecturer record in your database handling class
        // For example:
        AllController.NewLecturer(lectureID, lectureName, telephone, address, selectedCourse, selectedBranch);
        
        // Display a success message
        JOptionPane.showMessageDialog(this, "Lecturer updated successfully.", "Update Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception ex) {
        // If an error occurs during update, display an error message
        JOptionPane.showMessageDialog(this, "Error updating lecturer: " + ex.getMessage(), "Update Error", JOptionPane.ERROR_MESSAGE);
    }
       
        
        
        
    }//GEN-LAST:event_updateActionPerformed

