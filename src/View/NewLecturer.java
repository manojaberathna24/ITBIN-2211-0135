    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        ResultSet r = new DBSearch().SearchLecturer();
        DefaultTableModel dtm = (DefaultTableModel)jTable3.getModel();
        dtm.setRowCount(0); //set the no. of rows in the model
        
        try {
            Vector v;
            while(r.next()){
            v = new Vector();
            v.add(r.getString("Lecture_ID"));
            v.add(r.getString("Lecturer_Name"));
            v.add(r.getString("Telephone"));
            v.add(r.getString("Address"));
            v.add(r.getString("Course"));
            v.add(r.getString("Branch"));
           
            dtm.addRow(v); //Add a row to the end of the model
        }
            DBConnection.closeCon();
        }
        catch(Exception e){
           e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

